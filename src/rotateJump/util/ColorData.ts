namespace rotateJump {
    export class ColorData {
        public r: number = 0;
        public g: number = 0;
        public b: number = 0;
        public a: number = 1;

        constructor(r: number, g: number, b: number, a: number) {
            this.r = r;
            this.g = g;
            this.b = b;
            this.a = a;
        }
    }
}